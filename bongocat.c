    // assumes 1 frame prep stage
    void animation_phase(void) 
    {
        // render_time();
        //BongoCat_Rev1
        // oled_set_cursor(19.5, 1.5);

        //BongoCat_Rev2
        oled_set_cursor(24, 2);

        eval_anim_state();

        switch (anim_state)
        {
            case Idle:
                oled_write_raw_P(idle[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE_BONGOCAT);
                if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION_BONGOCAT)
                {
                    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                    anim_timer = timer_read32();
                }
                break;

            case Prep:
                oled_write_raw_P(prep[0], ANIM_SIZE_BONGOCAT);
                break;

            case Tap:
                oled_write_raw_P(tap[abs((TAP_FRAMES - 1) - current_tap_frame)], ANIM_SIZE_BONGOCAT);
                current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
                break;

        default:
            break;
        }
    }
    oled_set_cursor(0, 1);
    char heart[] = " JJ \x03" " NN";
    // char heart[] = " JJ NN";
    oled_write(heart, false);
    

    if (get_current_wpm() != 000) {
        oled_on(); // not essential but turns on animation OLED with any alpha keypress
        if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION_BONGOCAT) {
            anim_timer = timer_read32();
            animation_phase();
        }
        anim_cat_sleep = timer_read32();
    } else {
        if (timer_elapsed32(anim_cat_sleep) > OLED_TIMEOUT) {
            oled_off();
        } else {
            if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION_BONGOCAT) {
                anim_timer = timer_read32();
                animation_phase();
            }
        }
    }

    
